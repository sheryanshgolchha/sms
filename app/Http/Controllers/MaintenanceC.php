    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
          'month_year'       => 'required|unique:maintenances',
          'rate' => 'required|numeric',
        ]);
        $maintenance = Maintenance::updateOrCreate(['maintenance_id' => $request->post('maintenance_id')], [
                  'rate' => $request->post('rate'),
                  'month_year' => $request->post('month_year')
                ]);
        return response()->json(['code'=>200, 'message'=>'Maintenance Created successfully','data' => $maintenance], 200);
    }

